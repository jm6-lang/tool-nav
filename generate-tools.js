// 获取域名（去掉协议前缀）
function getDomain(url) {
  return url.replace(/^https?:\/\//, '').split('/')[0];
}

// 获取tag对应的颜色
function getTagColor(tag) {
  return tagColors[tag] || 'background:#e5e7eb;color:#6b7280';
}

// 获取分类名称
function getCatName(cat) {
  return catNames[cat] || cat;
}

// 生成单个工具的Markdown内容
function generateToolMarkdown(tool) {
  const domain = getDomain(tool.url);
  const tagColor = getTagColor(tool.tag);
  const catName = getCatName(tool.cat);

  // 生成核心功能部分
  const featuresContent = tool.features.map(f => `**${f.split('—')[0].trim()}** — ${f.split('—').slice(1).join('—').trim()}`).join('\n\n');

  // 生成适用场景部分
  const scenariosContent = tool.scenarios.map(s => `**${s.split('—')[0].trim()}** — ${s.split('—').slice(1).join('—').trim()}`).join('\n\n');

  // 生成使用技巧部分
  const tipsContent = tool.tips.map(t => `${t.split('—')[0].trim()} — ${t.split('—').slice(1).join('—').trim()}`).join('\n\n');

  return `---
title: ${tool.name}
description: ${tool.desc.substring(0, 80)}...
---

<div class="tool-header">
<div class="tool-icon"><img src="https://www.google.com/s2/favicons?domain=${domain}&sz=128" alt="" class="tool-favicon-img" loading="lazy" /></div>
<div class="tool-info">
<div class="tool-title-row">
<h1 class="tool-title">${tool.name}</h1>
<span class="tool-tag" style="${tagColor}">${tool.tag}</span>
</div>
<div class="tool-cat">${catName}</div>
</div>
</div>

<div class="redirect-notice">
<div class="notice-icon"><img src="https://www.google.com/s2/favicons?domain=${domain}&sz=128" alt="" class="notice-favicon" /></div>
<div class="notice-body">
<h3>即将离开 AI工具导航</h3>
<p>您正在访问 <strong>${domain}</strong></p>
<p class="notice-desc">我们已对目标网站进行基础审核，但不对其内容和服务负责。</p>
</div>
<div class="notice-btn">
<a href="${tool.url}" target="_blank" rel="noopener noreferrer" class="btn-confirm">确认访问 →</a>
<a href="/" class="btn-back">← 返回</a>
</div>
</div>

## 工具简介

${tool.intro}

## 核心功能

${featuresContent}

## 适用场景

${scenariosContent}

## 使用技巧

${tipsContent}

## 优缺点简评

**优势：** ${tool.pros}

**不足：** ${tool.cons}

<div class="tool-footer">
<a href="${tool.url}" target="_blank" rel="noopener noreferrer" class="visit-btn">🔗 访问 ${tool.name} 官网</a>
</div>`;
}

// 确保tools目录存在
const toolsDir = path.join(__dirname, '..', 'docs', 'tools');
if (!fs.existsSync(toolsDir)) {
  fs.mkdirSync(toolsDir, { recursive: true });
}

// 生成所有工具的Markdown文件
tools.forEach(tool => {
  const filePath = path.join(toolsDir, `${tool.id}.md`);
  const content = generateToolMarkdown(tool);
  fs.writeFileSync(filePath, content, 'utf8');
  console.log(`Created: ${filePath}`);
});

